#pragma once

#include <vector>

template <typename T> struct SmallSet {
  std::vector<T> data;
  SmallSet(int);
  typename std::vector<T>::iterator &begin();
  typename std::vector<T>::iterator &begin() const;
  typename std::vector<T>::iterator &end();
  typename std::vector<T>::iterator &end() const;
  void clear();
  template <typename U> void emplace(U);
  void erase(T const &);
  void erase(typename std::vector<T>::iterator const &);
  typename std::vector<T>::iterator &find(T const &);
  bool contains(T const &);
  bool empty();
  SmallSet &operator=(SmallSet &&);
  T &operator[](int);
  std::size_t size() const;
};